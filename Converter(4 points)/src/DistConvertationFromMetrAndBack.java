public class DistConvertationFromMetrAndBack {

    public static void DistMetrToKilometr(double num) {
        System.out.println(num + " Метр(a/ов) равно - " + (num / 1000) + " километр(а/ов)");
    }

    public static void DistKilometrToMetr(double num) {
        System.out.println(num + " Километр(a/ов) равно - " + (num / 1000) + " метр(а/ов)");
    }


    public static void DistMetrToYard(double num) {
        System.out.println(num + " Метр(a/ов) равно - " + (num * 1.0936) + " Ярд(а/ов)");
    }

    public static void DistYardToMetr(double num) {
        System.out.println(num + " Ярд(a/ов) равно - " + (num / 1.0936) + " метр(а/ов)");
    }


    public static void DistMetrToMilya(double num) {
        System.out.println(num + " Метр(a/ов) равно - " + (num * 0.00062137) + " миль(и)");
    }

    public static void DistMilyaToMetr(double num) {
        System.out.println(num + " Миль(и) равно - " + (num / 0.00062137) + " метр(а/ов)");
    }


    public static void DistMetrToDyuym(double num) {
        System.out.println(num + " Метр(a/ов) равно - " + (num * 39.370) + " Дюйм(а/ов)");
    }

    public static void DistDyuymToMetr(double num) {
        System.out.println(num + " Дюйм(a/ов) равно - " + (num / 39.370) + " метр(а/ов)");
    }
}
